import streamlit as st
import pandas as pd
import numpy as np
import pickle

# Load saved model and preprocessor pipeline
with open("best_model.pkl", "rb") as f:
    model = pickle.load(f)

# Streamlit UI
st.set_page_config(page_title="Medical Insurance Cost Estimator", layout="centered")
st.title("ðŸ’° Medical Insurance Cost Predictor")
st.write("Enter your personal and lifestyle info to estimate your expected insurance charges.")

# Input form
age = st.number_input("Age", min_value=18, max_value=100, step=1)
sex = st.selectbox("Sex", ['male', 'female'])
bmi = st.number_input("BMI (Body Mass Index)", min_value=10.0, max_value=60.0, step=0.1)
children = st.number_input("Number of Children", min_value=0, max_value=10, step=1)
smoker = st.selectbox("Do you smoke?", ['yes', 'no'])
region = st.selectbox("Region", ['northeast', 'northwest', 'southeast', 'southwest'])

# Predict button
if st.button("Predict Insurance Cost"):
    # Collect input into DataFrame
    input_data = pd.DataFrame({
        'age': [age],
        'sex': [sex],
        'bmi': [bmi],
        'children': [children],
        'smoker': [smoker],
        'region': [region]
    })

    # Predict
    prediction = model.predict(input_data)[0]

    st.success(f"ðŸ’¡ Estimated Insurance Cost: **${prediction:,.2f}**")